package array;

public class ArrayLearningUsingNewKeyword {

	public static void main(String[] args) {
		//two ways of declaring an array
		
		
		  int[] intArry = new int[6];    //one of the way to declare an array
		
		  //initialization of an array
		  
		  intArry[0] = 1;
		  intArry[1] = 2;
		  intArry[2] = 3;
		  intArry[3] = 4;
		  intArry[4] = 5;
		  intArry[5] = 6;
		  
		  for(int i =0; i<6;i++)
			  System.out.println(intArry[i]);
	}	  
}
